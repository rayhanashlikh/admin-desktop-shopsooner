    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String nama = txtNama.getText();
        String email = txtEmail.getText();
        String telepon = txtTelepon.getText();
        String password = new String(txtPassword.getPassword());
        String konfirmasi = new String(txtKonfirmasi.getPassword());
        String nomorRekening = txtRekening.getText();
        String alamat = txtAlamat.getText();
        
        boolean pass_validation = true;
        ArrayList<String> msg = new ArrayList<String>();
        
        if (nama.isEmpty()) {
            pass_validation = false;
            msg.add("Input nama tidak boleh kosong");
        }
        
        if (email.isEmpty()) {
            pass_validation = false;
            msg.add("Input email tidak boleh kosong");
        }
        
        if (telepon.isEmpty()) {
            pass_validation = false;
            msg.add("Input telepon tidak boleh kosong");
        }
        
        if (password.isEmpty()) {
            pass_validation = false;
            msg.add("Input password tidak boleh kosong");
        }
        
        if (konfirmasi.isEmpty()) {
            pass_validation = false;
            msg.add("Input konfirmasi password tidak boleh kosong");
        }
        
        if (nomorRekening.isEmpty()) {
            pass_validation = false;
            msg.add("Input nomor rekening tidak boleh kosong");
        }
        
        if (alamat.isEmpty()) {
            pass_validation = false;
            msg.add("Input alamat tidak boleh kosong");
        }
        
        try {
            if (pass_validation == true) {
                if (password.equals(konfirmasi)) {
                    stm = con.createStatement();
                    String hash = BCrypt.hashpw(password, BCrypt.gensalt(12));
                    String sql = "INSERT INTO sellers ("
                            + "id, nama, email, password, no_telp, alamat, no_rekening) VALUES(default, "
                            + "'"+ nama +"', '"+ email +"', '" + hash + "', '"
                            + telepon + "', '" + alamat  + "', '" + nomorRekening + "');";
                    
                    stm.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Tambah Data Seller berhasil");
                    FormSellers sellers = new FormSellers();
                    sellers.setVisible(true);
                    
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Password dan konfirmasi password harus sama");
                }
            } else {
                String message = "";
                for (String i : msg) {
                    message += "• " + i + "\n";
                }
                
                JOptionPane.showMessageDialog(null, message);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

